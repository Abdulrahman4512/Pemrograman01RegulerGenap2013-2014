package latihan04b.sesi1.bin;

public class icrement
{
	public static void main (String[] args)
	{
		int a;
		a=120;
		System.out.println("Contoh postfix icrement :");
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		a=120;
		System.out.println("Contoh postfix icrement :");
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		a=120;
		System.out.println("Contoh postfix icrement :");
		System.out.println(a);
		System.out.println(a--);
		System.out.println(a);
		a=120;
		System.out.println("Contoh postfix icrement :");
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
	}
}